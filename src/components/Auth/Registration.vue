<template> 
    <v-container fluid fill-height>
        <v-layout align-center justify-center> 
            <v-flex xs12 sm8 md4>
            <v-card class="elevetion-12">
                <v-toolbar dark color="#800080">
<v-toolbar-title>Registration</v-toolbar-title>
</v-toolbar>
                <v-card-text>
                    <v-form v-model="valid" ref="form" lazy-validation>
                        <v-text-field
                         prepend-icon="mdi-account" color="#800080" 							
                         name="email" 
                           label="Email" 
                         type="email"
                         v-model="email"
                         :rules="emailRules">
                        </v-text-field>
                        <v-text-field  
                         prepend-icon="mdi-lock" color="#800080" 
                         name="password" 
                         label="Password" 
                         type="password" 
                         v-model="password"
                         :rules="passwordRules">
                        </v-text-field>
                        <v-text-field
                         prepend-icon="mdi-lock" color="#800080"
                         name="confirm-password" 
                         label="Confirm Password" 
                         type="password"
                         v-model="confirmPassword"
                         :rules="confirmPasswordRules">
                        </v-text-field>
                    </v-form> 
                </v-card-text>
                <v-card-actions>
                    <v-spacer></v-spacer>
                    <v-btn color="purple" @click="onSubmit" :disabled="!valid">Create Account</v-btn>
                </v-card-actions>	
            </v-card>
            </v-flex>
        </v-layout> 
    </v-container>
</template>
<script>
export default {
    data () { 
        return {
            valid: false,
            email: "",
            password: "",
            confirmPassword: "",
            emailRules: [
            v => !!v || 'E-mail is required',
            v => /.+@.+\..+/.test(v) || 'E-mail must be valid'
            ],
            passwordRules: [
            v => !!v || 'Password is required',
            v => (v && v.length >= 6) || 'Password must be more or equel than 6 characters'
            ],
            confirmPasswordRules: [
            v => !!v || 'Password is required',
            v => v === this.password || 'Password should match'
            ]
        } 	
    },
    methods: {
    onSubmit(){
        if (this.$refs.form.validate()){
                const user = {
                email: this.email,
                password: this.password
            }
            console.log(user)
        }
    }
  }
} 
</script>





